// switch case (escolha caso)

let idade = Number(prompt("Insira sua idade: "));

switch (idade) 
{
    case 10:
        console.log("Você tem 10 anos!");
        break; // necessário para ele sair da estrutura
    case 15:
        console.log("Você tem 15 anos!");
        break // necessário para ele sair da estrutura
    default:
        console.log("Default");
        break; // necessário para ele sair da estrutura
}